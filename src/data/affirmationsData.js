/**
 * Get all affirmations from all categories as a flat array
 */
export const getAllAffirmations = () => {
    return affirmationCategories.flatMap(cat =>
        cat.affirmations.map(text => ({
            text,
            categoryId: cat.id,
            categoryName: cat.name,
            categoryIcon: cat.icon,
            categoryColor: cat.color
        }))
    );
};

/**
 * Get affirmations from specific categories
 */
export const getAffirmationsByCategories = (categoryIds) => {
    if (!categoryIds || categoryIds.length === 0) {
        return getAllAffirmations();
    }
    return affirmationCategories
        .filter(cat => categoryIds.includes(cat.id))
        .flatMap(cat =>
            cat.affirmations.map(text => ({
                text,
                categoryId: cat.id,
                categoryName: cat.name,
                categoryIcon: cat.icon,
                categoryColor: cat.color
            }))
        );
};

/**
 * Get a random affirmation from selected categories (or all if none selected)
 */
export const getRandomAffirmation = (categoryIds = []) => {
    const affirmations = getAffirmationsByCategories(categoryIds);
    return affirmations[Math.floor(Math.random() * affirmations.length)];
};

/**
 * Get category by ID
 */
export const getCategory = (categoryId) => {
    return affirmationCategories.find(c => c.id === categoryId);
};

